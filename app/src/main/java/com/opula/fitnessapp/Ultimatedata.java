package com.opula.fitnessapp;

public class Ultimatedata  {

    int img;
    String rate;

    public Ultimatedata(int img, String rate) {
        this.img = img;
        this.rate = rate;
    }

    public int getImg() {

        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getRate() {

        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }
}
